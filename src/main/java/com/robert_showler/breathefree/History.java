package com.robert_showler.breathefree;

public class History {

    private String date, time, beforeExercise, afterExercise, feedback;


    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getBeforeExercise() {
        return beforeExercise;
    }

    public void setBeforeExercise( String beforeExercise) {
        this.beforeExercise = beforeExercise;
    }

    public String getAfterExercise() {
        return afterExercise;
    }

    public void setAfterExercise(String afterExercise) {
        this.afterExercise = afterExercise;
    }

    public String getFeedback() {
        return feedback;
    }

    public void setFeedback(String feedback) {
        this.feedback = feedback;
    }
}
